package consultation_15.classwork0606.collections;

public class Car {

    private String name;
    private String age;
    private String type;

    public Car(String name, String age, String type) {
        this.name = name;
        this.age = age;
        this.type = type;
    }



    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getAge() {
        return age;
    }

    public void setAge(String age) {
        this.age = age;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }
}
